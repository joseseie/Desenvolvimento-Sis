    private void jButtonGerarDuplicatasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGerarDuplicatasActionPerformed
       int j = 1;
       String datavenc = jTextFieldData.getText();
       String dia, mes, ano;
       dia = ""+datavenc.charAt(0)+datavenc.charAt(1)+"/";
       mes = ""+datavenc.charAt(3)+datavenc.charAt(4);
       ano = "/"+datavenc.charAt(6)+datavenc.charAt(7)+datavenc.charAt(8)+datavenc.charAt(9);
       int anoInt = Integer.parseInt(""+datavenc.charAt(6)+datavenc.charAt(7)+datavenc.charAt(8)+datavenc.charAt(9));
       int mesInt = Integer.parseInt(mes);
  //     JOptionPane.showMessageDialog(null, "Gerar duplicatas clicado!");
       while(j < cont){
           if(mesInt < 10){
               mod.setCodVenda(Integer.parseInt(jTextFieldCodigoVenda.getText()));
               mod.setValorParcela(valorParcela+ (valorParcela * (porc / 100)));
               mod.setValor_total(Float.parseFloat(jTextFieldValorTotal.getText()));
               mod.setValor_venda(Float.parseFloat(jTextFieldValorVenda.getText()));
               mod.setNumeroParc(j);
               mod.setDataVenc(dia + "0" + mesInt + "/" + anoInt);
               controle.salvar(mod);
               
           } else {
               mod.setCodVenda(Integer.parseInt(jTextFieldCodigoVenda.getText()));
               mod.setValorParcela(valorParcela + (valorParcela * (porc / 100)));
               mod.setValor_total(Float.parseFloat(jTextFieldValorTotal.getText()));
               mod.setValor_venda(Float.parseFloat(jTextFieldValorVenda.getText()));
               mod.setNumeroParc(j);
               mod.setDataVenc(dia + mesInt + "/" + anoInt);
               controle.salvar(mod);
           }
           mesInt ++;
           if(mesInt > 12){
               anoInt ++;
               mesInt = 1;
           }
           j++;
       }
       JOptionPane.showMessageDialog(null, "Parcelas Geradas!.");
    }//GEN-LAST:event_jButtonGerarDuplicatasActionPerformed

    public void preencherTabela(){
        ArrayList dados = new ArrayList();
        
        String[] colunas = new String[]{"Codigo","Data", "Valor"};
        float valorActParcela = 0, valorTotal = 0;
        String data = jTextFieldData.getText();
        String dia,mes,ano;
        dia = ""+data.charAt(0)+data.charAt(1)+"/";
        mes = ""+data.charAt(3)+data.charAt(4);
        ano = "/"+data.charAt(6)+data.charAt(7)+data.charAt(8)+data.charAt(9);
        
        int anoInt = Integer.parseInt(""+data.charAt(6)+data.charAt(7)+data.charAt(8)+data.charAt(9)); //Conversao de dia em inteiro
        int mesInt = Integer.parseInt(mes);// conversao de mes em inteiro
        
       valorActParcela = valorParcela + (valorParcela*(porc/100));
             
            while(i <= qtdParcela){
             
               valorTotal = valorTotal + valorActParcela;
                  if( mesInt < 10){
                      dados.add(new Object[] {i,dia + "0" + mesInt + "/"+anoInt, valorActParcela });
                  } else{
                      dados.add(new Object[] {i, dia + mesInt + "/"+ anoInt, valorActParcela});
                  }
                  mesInt++;
                  if(mesInt > 12){
                      anoInt ++;
                      mesInt = 1;
                  }
                i++;
            } 

        jTextFieldValorTotal.setText(String.valueOf(valorTotal));
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        tabela.setModel(modelo);
        tabela.getColumnModel().getColumn(0).setPreferredWidth(100);
        tabela.getColumnModel().getColumn(0).setResizable(false);
        tabela.getColumnModel().getColumn(1).setPreferredWidth(230);
        tabela.getColumnModel().getColumn(1).setResizable(false);
        tabela.getColumnModel().getColumn(2).setPreferredWidth(220);
        tabela.getColumnModel().getColumn(2).setResizable(false);
        
        tabela.getTableHeader().setReorderingAllowed(false);
        tabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        
    }
    
