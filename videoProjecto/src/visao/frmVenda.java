    private void jButtonBuscaProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscaProdutoActionPerformed
        conn.conexao();
        try {
            PreparedStatement pst = conn.conn.prepareStatement("insert into venda(valor_venda)values(?)");
            pst.setFloat(1, 0);
            pst.execute();
            conn.executaSQL("select * from venda");
            conn.rs.last();
            codVenda = conn.rs.getInt("id_venda"); 
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao caregar"+ex);
        }
        flag = 2;
        preencherTabelaProduto("select * from produto where nome_produto like'%"+jTextFieldProduto.getText()+"%'");
    }//GEN-LAST:event_jButtonBuscaProdutoActionPerformed

    private void tabelaPesquisaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaPesquisaMouseClicked
        if(flag == 1){
            String nome_cliente = ""+tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 1);
            jTextFieldCliente.setText(nome_cliente);
        } else {
            try {
                String nome_produto= ""+tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 1);
                conn.executaSQL("select * from produto where nome_produto='"+nome_produto+"'");
                conn.rs.first();
                preco_produto = conn.rs.getFloat("preco_venda");
                jTextFieldProduto.setText(nome_produto);
                jTextFieldValPorItem.setText(String.valueOf(preco_produto));
                jTextFieldQuantidade.setText("1");
                conn.desconecta();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao selecionar o produto");
            }
        }
    }//GEN-LAST:event_tabelaPesquisaMouseClicked

