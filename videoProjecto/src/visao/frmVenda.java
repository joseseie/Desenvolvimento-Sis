    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        try {
            int idVenda, quantidade= 0;
            conn.conexao();
            conn.executaSQL("select * from produto where nome_produto='"+jTextFieldProduto.getText()+"'");
            conn.rs.first();
            quantidade = conn.rs.getInt("quantidade");
            if(quantidade > Integer.parseInt(jTextFieldQuantidade.getText())){
                int result = quantidade - Integer.parseInt(jTextFieldQuantidade.getText());
                mod.setNomeProduto(jTextFieldProduto.getText());
                mod.setQtdItem(Integer.parseInt(jTextFieldQuantidade.getText()));
                mod.setIdVenda(codVenda);
                controle.adicionaItem(mod);
                
                preencherItensVenda("select * from produto inner join itens_venda__produto on "
                        + "produto.id_produto=itens_venda__produto.id_produto inner join venda on "
                        + "venda.id_venda=itens_venda__produto.id_venda where venda.id_venda="+codVenda);
            } else {
                JOptionPane.showMessageDialog(null, "A quantidade desejada nao esta disponivel no estoque!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

