    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        try {
            conex.executaSQL("select * from login where login='"+jTextFieldNome.getText()+"'");
            conex.rs.first();
            if(conex.rs.getString("senha").equals(jPasswordFieldPassord.getText())){
                
                conex.executaSQL("select * from vencimento");
                conex.rs.last();
                
                SimpleDateFormat df = new SimpleDateFormat("ddMMyyyy");
                Date hoje = new Date();
                String dataActual = df.format(hoje);
                String dataSistema = conex.rs.getString("datavenc");
                char[] dataA = dataActual.toCharArray();
                char[] dataS = dataSistema.toCharArray();
                
                int diaAt, mesAt, anoAt, diaVenc, mesVenc, anoVenc;
                diaAt = Integer.parseInt((""+dataA[0]+dataA[1]));
                mesAt = Integer.parseInt(""+dataA[2]+dataA[3]);
                anoAt = Integer.parseInt(""+dataA[4]+dataA[5]+dataA[6]+dataA[7]);
                
                diaVenc = Integer.parseInt((""+dataS[0]+dataS[1]));
                mesVenc = Integer.parseInt(""+dataS[2]+dataS[3]);
                anoVenc = Integer.parseInt(""+dataS[4]+dataS[5]+dataS[6]+dataS[7]);
                
                if(anoAt < anoVenc){
                    new Principal(jTextFieldNome.getText()).setVisible(true);
                } else if(anoAt == anoVenc) {
                    if(mesAt < mesVenc){
                        new Principal(jTextFieldNome.getText()).setVisible(true);
                    } else if(mesAt == mesVenc) {
                        if(diaAt <= diaVenc){
                            new Principal(jTextFieldNome.getText()).setVisible(true);
                        } else {
                           new frmValidaSistema().setVisible(true); 
                        }
                    } else {
                        new frmValidaSistema().setVisible(true);
                    }
                } else {
                    new frmValidaSistema().setVisible(true);
                }


                dispose();
               
            } else {
                JOptionPane.showMessageDialog(null, "Usuario e senha n√£o correspondem!");
            }
          
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Usuario e senha n√£o correspondem!");
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

